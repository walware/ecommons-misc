	public static String getEntityName(final String entity) {
		final int end= entity.length();
		if (end >= 2 && entity.charAt(0) == '&') {
			return entity.substring(1, (entity.charAt(end - 1) == ';') ? (end - 1) : end);
		}
		return entity;
	}
	
